    private void jButton1MouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_jButton1MouseClicked
        String usuario = user.getText();
        String password = pass.getText();
        int id = Integer.parseInt(idUser.getText());
        int tipo = tipoUsuario.getSelectedIndex();
        Usuario u = new Usuario(usuario,password,id,tipo);
        tienda.getListaUsuarios().agregarUsuario(u);
        EscribirUsuario(usuario,password,id,tipo);
        if(tienda.getListaUsuarios().buscarUsuario(u) == true){
            VentanaUserCreado ven = new VentanaUserCreado();
            ven.setVisible(true);
        } 
    }//GEN-LAST:event_jButton1MouseClicked
